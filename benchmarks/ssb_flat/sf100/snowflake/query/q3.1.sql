SELECT
    C_NATION,
    S_NATION,
    FLOOR(LO_ORDERDATE / 10000) AS year,
    sum(LO_REVENUE) AS revenue
FROM LINEORDER_FLAT
WHERE
    C_REGION = 'ASIA'
  AND S_REGION = 'ASIA'
  AND LO_ORDERDATE >= 19920101
  AND LO_ORDERDATE <= 19971231
GROUP BY
    C_NATION,
    S_NATION,
    year
ORDER BY
    year ASC,
    revenue DESC;