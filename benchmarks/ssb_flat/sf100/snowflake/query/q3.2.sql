SELECT
    C_CITY,
    S_CITY,
    FLOOR(LO_ORDERDATE / 10000) AS year,
    sum(LO_REVENUE) AS revenue
FROM LINEORDER_FLAT
WHERE
    C_NATION = 'UNITED STATES'
  AND S_NATION = 'UNITED STATES'
  AND LO_ORDERDATE >= 19920101
  AND LO_ORDERDATE <= 19971231
GROUP BY
    C_CITY,
    S_CITY,
    year
ORDER BY
    year ASC,
    revenue DESC;