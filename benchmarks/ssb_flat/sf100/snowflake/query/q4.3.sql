SELECT
    FLOOR(LO_ORDERDATE / 10000) AS year,
    S_CITY,
    P_BRAND,
    sum(LO_REVENUE - LO_SUPPLYCOST) AS profit
FROM
    LINEORDER_FLAT
WHERE
    S_NATION = 'UNITED STATES'
  AND (LO_ORDERDATE >= 19970101 AND LO_ORDERDATE <= 19981231)
  AND P_CATEGORY = 'MFGR#14'
GROUP BY
    year,
    S_CITY,
    P_BRAND
ORDER BY
    year ASC,
    S_CITY ASC,
    P_BRAND ASC;