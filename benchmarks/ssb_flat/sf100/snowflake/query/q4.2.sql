SELECT
    FLOOR(LO_ORDERDATE / 10000) AS year,
    S_NATION,
    P_CATEGORY,
    sum(LO_REVENUE - LO_SUPPLYCOST) AS profit
FROM LINEORDER_FLAT
WHERE
    C_REGION = 'AMERICA'
  AND S_REGION = 'AMERICA'
  AND (LO_ORDERDATE >= 19970101 AND LO_ORDERDATE <= 19981231)
  AND (P_MFGR = 'MFGR#1' OR P_MFGR = 'MFGR#2')
GROUP BY
    year,
    S_NATION,
    P_CATEGORY
ORDER BY
    year ASC,
    S_NATION ASC,
    P_CATEGORY ASC;