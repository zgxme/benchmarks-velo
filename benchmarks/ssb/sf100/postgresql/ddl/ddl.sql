-- SSB (Star Schema Benchmark) DDL for PostgreSQL
-- Scale Factor: 100 (~100GB)

-- NOTE:
-- Per benchmark requirement: do NOT create any indexes (including PRIMARY KEY)
-- to avoid index maintenance cost during data loading.

-- Drop existing indexes/PKs from prior runs so COPY doesn't maintain them.
DROP INDEX IF EXISTS idx_lo_orderdate;
DROP INDEX IF EXISTS idx_lo_custkey;
DROP INDEX IF EXISTS idx_lo_partkey;
DROP INDEX IF EXISTS idx_lo_suppkey;
ALTER TABLE IF EXISTS lineorder DROP CONSTRAINT IF EXISTS lineorder_pkey;
ALTER TABLE IF EXISTS customer DROP CONSTRAINT IF EXISTS customer_pkey;
ALTER TABLE IF EXISTS part DROP CONSTRAINT IF EXISTS part_pkey;
ALTER TABLE IF EXISTS supplier DROP CONSTRAINT IF EXISTS supplier_pkey;
ALTER TABLE IF EXISTS date DROP CONSTRAINT IF EXISTS date_pkey;

CREATE TABLE IF NOT EXISTS customer
(
    C_CUSTKEY       INTEGER NOT NULL,
    C_NAME          VARCHAR(25) NOT NULL,
    C_ADDRESS       VARCHAR(25) NOT NULL,
    C_CITY          VARCHAR(10) NOT NULL,
    C_NATION        VARCHAR(15) NOT NULL,
    C_REGION        VARCHAR(12) NOT NULL,
    C_PHONE         VARCHAR(15) NOT NULL,
    C_MKTSEGMENT    VARCHAR(10) NOT NULL
);

CREATE TABLE IF NOT EXISTS lineorder
(
    LO_ORDERKEY             BIGINT NOT NULL,
    LO_LINENUMBER           INTEGER NOT NULL,
    LO_CUSTKEY              INTEGER NOT NULL,
    LO_PARTKEY              INTEGER NOT NULL,
    LO_SUPPKEY              INTEGER NOT NULL,
    LO_ORDERDATE            INTEGER NOT NULL,
    LO_ORDERPRIORITY        VARCHAR(15) NOT NULL,
    LO_SHIPPRIORITY         VARCHAR(1) NOT NULL,
    LO_QUANTITY             INTEGER NOT NULL,
    LO_EXTENDEDPRICE        BIGINT NOT NULL,
    LO_ORDTOTALPRICE        BIGINT NOT NULL,
    LO_DISCOUNT             INTEGER NOT NULL,
    LO_REVENUE              BIGINT NOT NULL,
    LO_SUPPLYCOST           BIGINT NOT NULL,
    LO_TAX                  INTEGER NOT NULL,
    LO_COMMITDATE           INTEGER NOT NULL,
    LO_SHIPMODE             VARCHAR(10) NOT NULL
);

CREATE TABLE IF NOT EXISTS part
(
    P_PARTKEY       INTEGER NOT NULL,
    P_NAME          VARCHAR(22) NOT NULL,
    P_MFGR          VARCHAR(6) NOT NULL,
    P_CATEGORY      VARCHAR(7) NOT NULL,
    P_BRAND         VARCHAR(9) NOT NULL,
    P_COLOR         VARCHAR(11) NOT NULL,
    P_TYPE          VARCHAR(25) NOT NULL,
    P_SIZE          INTEGER NOT NULL,
    P_CONTAINER     VARCHAR(10) NOT NULL
);

CREATE TABLE IF NOT EXISTS supplier
(
    S_SUPPKEY       INTEGER NOT NULL,
    S_NAME          VARCHAR(25) NOT NULL,
    S_ADDRESS       VARCHAR(25) NOT NULL,
    S_CITY          VARCHAR(10) NOT NULL,
    S_NATION        VARCHAR(15) NOT NULL,
    S_REGION        VARCHAR(12) NOT NULL,
    S_PHONE         VARCHAR(15) NOT NULL
);

CREATE TABLE IF NOT EXISTS date
(
    D_DATEKEY            INTEGER NOT NULL,
    D_DATE               VARCHAR(18) NOT NULL,
    D_DAYOFWEEK          VARCHAR(9) NOT NULL,
    D_MONTH              VARCHAR(9) NOT NULL,
    D_YEAR               INTEGER NOT NULL,
    D_YEARMONTHNUM       INTEGER NOT NULL,
    D_YEARMONTH          VARCHAR(7) NOT NULL,
    D_DAYNUMINWEEK       INTEGER NOT NULL,
    D_DAYNUMINMONTH      INTEGER NOT NULL,
    D_DAYNUMINYEAR       INTEGER NOT NULL,
    D_MONTHNUMINYEAR     INTEGER NOT NULL,
    D_WEEKNUMINYEAR      INTEGER NOT NULL,
    D_SELLINGSEASON      VARCHAR(12) NOT NULL,
    D_LASTDAYINWEEKFL    INTEGER NOT NULL,
    D_LASTDAYINMONTHFL   INTEGER NOT NULL,
    D_HOLIDAYFL          INTEGER NOT NULL,
    D_WEEKDAYFL          INTEGER NOT NULL
);
